void solve(){
    int n,k; cin>>n>>k;
    string s; cin>>s;
    vec<char> ans(n);
    for(int i=k;i<n;i++) ans[i]=s[i];
    int ptr1=0,ptr2=k-1;
    int curr=ptr1;
    for(int i=k-1;i>=0;i--){
        if(curr==ptr1){
            ans[i]=s[ptr1++];
            curr=ptr2;
        }
        else{
            ans[i]=s[ptr2--];
            curr=ptr1;
        }
    }
    for(char c:ans) cout<<c;
    cout<<endl;
}

